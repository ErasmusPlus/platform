  </h2>


  

  <p>Το ERASMUS+ είναι το νέο πρόγραμμα της Ευρωπαϊκής Επιτροπής για την εκπαίδευση, την κατάρτιση, τη νεολαία και τον αθλητισμό, που στοχεύει στην ενίσχυση των δεξιοτήτων και της απασχολησιμότητας καθώς και στον εκσυγχρονισμό των συστημάτων εκπαίδευσης, κατάρτισης και νεολαίας, σε όλους τους τομείς της Δια Βίου Μάθησης (Ανώτατη Εκπαίδευση, Επαγγελματική Εκπαίδευση και Κατάρτιση, Εκπαίδευση Ενηλίκων, Σχολική Εκπαίδευση, δραστηριότητες νεολαίας, κτλ).</p>
<p>Τα ισχύοντα προγράμματα χρηματοδότησης της ΕΕ για την εκπαίδευση, την κατάρτιση και τη νεολαία έληξαν στο τέλος του 2013, και το νέο πρόγραμμα ERASMUS+ έχει τεθεί σε ισχύ από την <strong>1η Ιανουαρίου του 2014</strong>.</p>
<p>Το νέο πρόγραμμα συνδυάζει όλα τα σημερινά προγράμματα της ΕΕ για την εκπαίδευση, την κατάρτιση και τη νεολαία όπως, μεταξύ άλλων, το ολοκληρωμένο Πρόγραμμα Δια Βίου Μάθησης (LLP) (Erasmus, Leonardo da Vinci, Comenius, Grundtvig), το πρόγραμμα «Νεολαία σε Δράση» και πέντε προγράμματα διεθνούς συνεργασίας (Erasmus Mundus, Tempus, Alfa, Edulink και τα προγράμματα συνεργασίας με τις βιομηχανικές χώρες).</p>
<p>Για πρώτη φορά το Erasmus+ θα προσφέρει υποστήριξη στον αθλητισμό, ειδικά το μαζικό. Το νέο Πρόγραμμα ολοκληρώνεται με την ειδική Δράση του Jean Monnet, που ενθαρρύνει τη διδασκαλία, την έρευνα και τη συζήτηση στους τομείς της ιστορίας, της πολιτικής, της οικονομίας και του Ευρωπαϊκού Δικαίου.</p>
<p>Το Πανεπιστήμιο Δυτικής Μακεδονίας είναι κάτοχος του Πανεπιστημιακού Χάρτη Erasmus 2014-2020.</p>
<p><a href="https://uowm.gr/wp-content/uploads/2016/02/Erasmus_Charter_2014-2020_UOWM.pdf" rel="">Erasmus Charter Πανεπιστημίου Δυτικής Μακεδονίας</a></p>
<p><strong>Institutional Erasmus Code</strong>: G KOZANI02</p>
<p><a href="https://uowm.gr/wp-content/uploads/2016/02/Erasmus_PolicyStatement_Overall_Strategy.pdf" rel="">Erasmus Policy Statement</a></p>
<p>Οι <strong>επιλέξιμες χώρες</strong> στο Πρόγραμμα Erasmus+ χωρίζονται σε δύο ομάδες:</p>
<ol>
<li><strong>Οι Χώρες που συμμετέχουν στο Πρόγραμμα (Programme Countries)</strong><br />
Οι ακόλουθες χώρες, συμπεριλαμβανομένων και των υπερπόντιων χωρών και των περιοχών των χωρών κρατών μελών της ΕΕ , μπορούν να συμμετέχουν πλήρως σε όλες τις δράσεις του Erasmus+:</p>
<ul>
<li><strong>Τα κράτη μέλη της Ευρωπαϊκής Ένωσης (ΕΕ)</strong><br />
Αυστρία, Εσθονία, Ιταλία, Πορτογαλία,Βέλγιο, Φινλανδία, Λετονία, Ρουμανία,Βουλγαρία, Γαλλία, Λιθουανία, Σλοβακία,Κροατία, Γερμανία, Λουξεμβούργο, Σλοβενία,Κύπρος, Ελλάδα, Μάλτα, Ισπανία, Δημοκρατία της Τσεχίας, Ολλανδία, Ουγγαρία, Σουηδία, Δανία, Ιρλανδία, Πολωνία, Ηνωμένο Βασίλειο.</li>
<li><strong>Xώρες που δεν ανήκουν στην Ευρωπαϊκή Ένωση (ΕΕ)</strong><br />
ΠΓΔΜ, Λιχτενστάιν, Τουρκία, Ισλανδία, Νορβηγία</li>
</ul>
</li>
<li><strong>Χώρες Εταίροι (Partner Countries)</strong><br />
Οι ακόλουθες χώρες μπορούν να λάβουν μέρος σε ορισμένες δράσεις στο πλαίσιο του Erasmus+, σύμφωνα με συγκεκριμένα κριτήρια και προϋποθέσεις. Περισσότερες πληροφορίες σχετικά με τα κριτήρια αυτά μπορείτε να αναζητήσετε στον <a href="http://www.iky.gr/erasmus-plus-evrwpaiki-prosklisi/odigos-programmatos-erusmus-plus" target="_blank">Οδηγό του Προγράμματος</a>.</p>
<ul>
<li><strong>Γειτονικές Χώρες Εταίροι της ΕΕ (Partner Countries Neighbouring EU)</strong>
<ul>
<li><strong>Ανατολικές Χώρες</strong>: Αρμενία, Αζερμπαϊτζάν, Λευκορωσία, Γεωργία, Μολδαβία, Ουκρανία</li>
<li><strong>Χώρες της νότιας Μεσογείου</strong>: Αλγερία, Αίγυπτος, Ισραήλ, Ιορδανία, Λίβανος, Λιβύη, Μαρόκο, Παλαιστίνη, Συρία, Τυνησία</li>
<li><strong>Χώρες Δυτικών Βαλκανίων</strong>: Αλβανία, Βοσνία και Ερζεγοβίνη , Κοσσυφοπέδιο, Μαυροβούνιο, Σερβία</li>
<li><strong>Άλλες Χώρες</strong>: Ρωσική Ομοσπονδία, Ελβετία (Μετά την ανακοίνωση της Ευρωπαϊκής Επιτροπής σχετικά με τη συμμετοχή της Ελβετίας στο Erasmus + , η Ελβετία θα συμμετέχει πλέον ως Χώρα Εταίρος (Partner Country) στο πλαίσιο του Erasmus + 2014 και όχι ως χώρα που συμμετέχει στο Πρόγραμμα, εν αναμονή σχετικής συμφωνίας μεταξύ της ΕΕ και της Ελβετίας).</li>
<li><strong>Άλλες Χώρες Εταίροι (Other Partner Countries)</strong><br />
Όλες οι άλλες χώρες στον κόσμο, είναι σε θέση να συμμετάσχουν σε ορισμένες ενέργειες στο πλαίσιο του Erasmus + υπό συγκεκριμένες συνθήκες . Λεπτομερή κριτήρια επιλεξιμότητας μπορείτε να αναζητήσετε στον <a href="http://www.iky.gr/erasmus-plus-evrwpaiki-prosklisi/odigos-programmatos-erusmus-plus" target="_blank">Οδηγό του Προγράμματος</a>.</li>
