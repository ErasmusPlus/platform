  <p>Το ERASMUS+ είναι το νέο πρόγραμμα της Ευρωπαϊκής Επιτροπής για την εκπαίδευση, την κατάρτιση, τη νεολαία και τον αθλητισμό, που στοχεύει στην ενίσχυση των δεξιοτήτων και της απασχολησιμότητας καθώς και στον εκσυγχρονισμό των συστημάτων εκπαίδευσης, κατάρτισης και νεολαίας, σε όλους τους τομείς της Δια Βίου Μάθησης (Ανώτατη Εκπαίδευση, Επαγγελματική Εκπαίδευση και Κατάρτιση, Εκπαίδευση Ενηλίκων, Σχολική Εκπαίδευση, δραστηριότητες νεολαίας, κτλ).</p>
<p>Τα ισχύοντα προγράμματα χρηματοδότησης της ΕΕ για την εκπαίδευση, την κατάρτιση και τη νεολαία έληξαν στο τέλος του 2013, και το νέο πρόγραμμα ERASMUS+ έχει τεθεί σε ισχύ από την <strong>1η Ιανουαρίου του 2014</strong>.</p>
<p>Το νέο πρόγραμμα συνδυάζει όλα τα σημερινά προγράμματα της ΕΕ για την εκπαίδευση, την κατάρτιση και τη νεολαία όπως, μεταξύ άλλων, το ολοκληρωμένο Πρόγραμμα Δια Βίου Μάθησης (LLP) (Erasmus, Leonardo da Vinci, Comenius, Grundtvig), το πρόγραμμα «Νεολαία σε Δράση» και πέντε προγράμματα διεθνούς συνεργασίας (Erasmus Mundus, Tempus, Alfa, Edulink και τα προγράμματα συνεργασίας με τις βιομηχανικές χώρες).</p>
<p>Για πρώτη φορά το Erasmus+ θα προσφέρει υποστήριξη στον αθλητισμό, ειδικά το μαζικό. Το νέο Πρόγραμμα ολοκληρώνεται με την ειδική Δράση του Jean Monnet, που ενθαρρύνει τη διδασκαλία, την έρευνα και τη συζήτηση στους τομείς της ιστορίας, της πολιτικής, της οικονομίας και του Ευρωπαϊκού Δικαίου.</p>
